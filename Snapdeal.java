package Action;

import java.time.Duration;
import java.util.List;

import org.openqa.selenium.By;
import org.openqa.selenium.WebElement;
import org.openqa.selenium.chrome.ChromeDriver;
import org.openqa.selenium.interactions.Actions;

import io.github.bonigarcia.wdm.WebDriverManager;

public class Snapdeal {
	public static void main(String[] args) {
		WebDriverManager.chromedriver().setup();
		ChromeDriver driver = new ChromeDriver();
		driver.get("https://www.snapdeal.com/");
		driver.manage().window().maximize();
		driver.manage().timeouts().implicitlyWait(Duration.ofSeconds(30));
		WebElement men = driver.findElement(By.className("catText"));
		Actions brand = new Actions(driver);
		brand.moveToElement(men).perform();
		driver.findElement(By.className("linkTest")).click();
		WebElement shoes = driver.findElement(By.xpath("//span[@class='category-name category-count']"));
		String shoescount = shoes.getText();
		System.out.println(shoescount);
		String title = driver.getTitle();
		System.out.println(title);
	}

}



















































































































































































































































































































































































































































































































